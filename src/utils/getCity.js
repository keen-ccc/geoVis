export const getCity = (arr,city) => {
    //返回value值
    // console.log(cities.find(item => item.label === city).value)
    for(const item of arr){
        if(item.label === city){
            console.log('item',item)
            return item.coords
        }
        if(item.children){
            const res = getCity(item.children,city)
            if(res) {
                return res
            }
        }
    }
    return null
}
export default getCity;